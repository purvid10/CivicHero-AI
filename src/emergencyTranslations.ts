export function getLocalizedEmergencyCenterName(
  englishName: string,
  langCode: string
): string {
  const dictionary = EMERGENCY_WORDS_TRANSLATIONS[langCode];
  if (!dictionary) return englishName;

  let localized = englishName.toLowerCase().trim();

  // Sort dictionary keys by length in descending order to avoid greedy/partial matches
  const sortedKeys = Object.keys(dictionary).sort((a, b) => b.length - a.length);

  for (const key of sortedKeys) {
    if (localized === key) {
      return dictionary[key];
    }
  }

  // If no direct exact match, let's replace sub-phrases or words
  let result = englishName;
  for (const key of sortedKeys) {
    const regex = new RegExp(`\\b${key}\\b`, 'gi');
    if (regex.test(result)) {
      result = result.replace(regex, dictionary[key]);
    }
  }

  return result;
}

export function getLocalizedDetailOrCapacity(
  englishText: string,
  langCode: string
): string {
  const dictionary = EMERGENCY_WORDS_TRANSLATIONS[langCode];
  if (!dictionary) return englishText;

  let localized = englishText.toLowerCase().trim();
  if (dictionary[localized]) {
    return dictionary[localized];
  }

  let result = englishText;
  const sortedKeys = Object.keys(dictionary).sort((a, b) => b.length - a.length);
  for (const key of sortedKeys) {
    const regex = new RegExp(`\\b${key}\\b`, 'gi');
    if (regex.test(result)) {
      result = result.replace(regex, dictionary[key]);
    }
  }

  return result;
}

