export function getLocalizedCitizenName(
  englishName: string,
  langCode: string
): string {
  const dictionary = CITIZEN_NAME_TRANSLATIONS[langCode];
  if (!dictionary) return englishName;

  // Exact match
  if (dictionary[englishName]) {
    return dictionary[englishName];
  }

  // Case-insensitive check/replace for substrings
  let result = englishName;
  for (const key of Object.keys(dictionary)) {
    const regex = new RegExp(key, 'gi');
    if (regex.test(result)) {
      result = result.replace(regex, dictionary[key]);
    }
  }

  return result;
}

