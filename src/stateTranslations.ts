export function getLocalizedStateName(stateIdOrName: string, langCode: string): string {
  const langTranslations = STATE_TRANSLATIONS[langCode] || STATE_TRANSLATIONS['en-US'];
  
  // If we passed an issue ID (like 'ind-issue-14' or 'metro-heights')
  if (langTranslations[stateIdOrName]) {
    return langTranslations[stateIdOrName];
  }
  
  // If we passed the English state name itself, try to find its ID first
  const enTranslations = STATE_TRANSLATIONS['en-US'];
  const matchEntry = Object.entries(enTranslations).find(
    ([_, val]) => val.toLowerCase() === stateIdOrName.toLowerCase()
  );
  
  if (matchEntry) {
    const id = matchEntry[0];
    return langTranslations[id] || stateIdOrName;
  }
  
  return stateIdOrName;
}
